// Switch Case Statement
/**
Switch case statement is another way to write
conditional statements in Javascript.
*/

let color = 'blue';

// if none of these conditions meet, the default statement is going to run.
switch (color) {
  case 'red':
    console.log('The color is red');
    break;
  case 'blue':
    console.log('The color is blue');
    break;
  default:
    console.log('The color is ambigious');
}
